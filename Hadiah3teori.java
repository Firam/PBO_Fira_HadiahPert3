class Hadiah3teori {
	public static void main (String[] args) {
		int aa = 5;
		short bb = 6;
		float cc = 0.5f;
		
		System.out.println("aa="+aa+",bb="+bb);
		System.out.println("aa+bb="+(aa+bb));
		System.out.println("aa*bb*cc="+(aa*bb*cc));
		System.out.println("bb/cc="+(bb/cc));
		System.out.println("aa="+aa+",bb="+bb+",cc="+cc);
	
	}
}